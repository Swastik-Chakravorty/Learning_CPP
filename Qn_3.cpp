#include<iostream>

using namespace std;

int main()
{
    cout << "Size of Character is: " << sizeof(char) << " bytes" << endl;
    cout << "Size of Short Integer is: " << sizeof(short) << " bytes" << endl;
    cout << "Size of Integer is: " << sizeof(int) << " bytes" << endl;
    cout << "Size of Long Integer is: " << sizeof(long) << " bytes" << endl;
    cout << "Size of Long Long Integer is: " << sizeof(long long) << " bytes" << endl;
    cout << "Size of Float is: " << sizeof(float) << " bytes" << endl;
    cout << "Size of Double is: " << sizeof(double) << " bytes" << endl;
    cout << "Size of Long Double is: " << sizeof(long double) << " bytes" << endl;
    cout << "Size of Bool is: " << sizeof(bool) << " bytes" << endl;
    return 0;
}